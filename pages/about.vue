      <h2 class="title is-3">Models, projections, uncertainties</h2>
      <h3 class="subtitle is-4">Climate models help us imagine possible climate futures.</h3>
      <div class="content is-size-5">
        

<p>We use weather forecasts for short-term planning.  Climate projections can be used for long-term planning—but they are not the same as forecasts.   Weather varies day to day and even minute to minute, whereas climate refers to  the average or typical conditions over much longer time periods.  However, there are uncertainties based on model limitations and unknown future human behavior that make long-range forecasting very different from predicting tomorrow's weather.</p>
<p>Climate projections look much further into the future than weather forecasts. They address uncertainties by focusing on the range of future conditions that would likely occur, given what we currently know about the climate system and how it will change in response to changes in the factors that affect it.</p>
</div>

<h3 class="subtitle is-4">How do we make projections? With climate models.</h3>
      <div class="content is-size-5">

<p>Climate models are simplified versions of reality that try to explain climatic processes with just the most necessary parts of the system. Their usefulness  is evident when we compare observed historical climate and simulated data —the models capture the most important climate patterns.</p>
<p>Climate models use data to calculate how the global climate varies. These data include:</p>
<ul>
  <li>initial climate conditions</li>
  <li>&ldquo;forcings&rdquo; such as atmospheric greenhouse gas, solar and volcanic variability</li>
  <li>ocean and atmosphere variability</li>
  <li>land surface conditions</li>
  <li>feedbacks such as the carbon cycle and the water cycle</li>
</ul>
<p>The end product is a simulation of future climate.  Because it is based on statistical probabilities, the data are most reliable when averaged across time or space, such as the projected average of 30 years of winter precipitation for your community, or the likely hottest temperature that might occur on the North Slope.</p>
</div>

<h3 class="subtitle is-4">Climate models aren&rsquo;t perfect, though.</h3>
<div class="content is-size-5">
<p>Between now and about 20&ndash;30 years from now, current climate change is the best predictor of the rate of change, but year to year variability is the largest source of uncertainty. Although long-term climate change shows clear trends, those can be masked by natural ups and downs in the short term.  Climate models do simulate this kind of variability, but they cannot predict it precisely.</p>
</div>

<h3 class="subtitle is-4">Best practices for making projections</h3>
<div class="content is-size-5">
  <ul>
    <li><strong>Use multiple decades.</strong>  Averages over 20&ndash;30 years are more resistant to transient variability in climate models and to natural variations in regional climate. Compare a future (like 2030&ndash;2069) to a historical reference frame (like 1970&ndash;1999), and keep in mind that the later the historical reference frame, the more climate change is already in it!</li>
    <li><strong>Use multiple climate models.</strong>  Picking one model is not good practice because all the models are at least plausible, if not equally likely. Use several separate models if the full range of possibilities is important to your work, or use the average of multiple models if you are more interested in the most likely outputs.  This is especially critical between now and about the 2050s or 2060s.</li>
    <li><strong>Use multiple emission scenarios.</strong>  Given the uncertainty around future human behavior, you should pick at least two scenarios that bracket the likely range, unless you are only interested in looking at the &ldquo;best case&rdquo; or &ldquo;worst case&rdquo;.  RCP 4.5 and RCP 8.5 are good choices.. This is especially critical after about 2050.</li>
    <li><strong>Look at medium&ndash;term and longer&ndash;term futures.</strong> Acomprehensive  assessment would consider a historical, a mid-21st century future, and a late&ndash;21<sup>st</sup> century future. The two futures should have a high, low and middle range each,  possibly with multiple models and multiple emission scenarios in each future window.</li>
    <li><strong>Don&rsquo;t make your assessment area too small.</strong>  The more local your assessment, the more likely it is that local factors like elevation, vegetation, etc. and the process used to downscale the climate model to local resolution contribute to the uncertainty. Larger areas are probably more resistant to this local variation, so a watershed, planning unit, or responses across several of these are perhaps more useful to consider.</li>
    <li><strong>For fire projections and post-fire vegetation, look at averages across many model runs.</strong> The ALFRESCO fire and vegetation model cannot predict the precise behavior of future fires, but it can simulate the likelihood that fires will start and spread across broad landscapes across long periods of time, causing shifts in the age and type of vegetation.  When looking at outputs, consider either a typical model run (&ldquo;best replicate&rdquo;) or an average of many possible burning scenarios.  Outputs are helpful for assessing large&ndash;scale long&ndash;term shifts, but are not meaningful at a pixel by pixel level.</li>
